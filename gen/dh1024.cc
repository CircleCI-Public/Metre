DH *get_dh1024()
{
    static unsigned char dhp_1024[] = {
	0xFC, 0x22, 0xA2, 0x6D, 0x55, 0x43, 0x92, 0x52, 0x50, 0x31, 
	0xA9, 0x1B, 0x15, 0xC2, 0x58, 0x1C, 0x40, 0xCF, 0xEE, 0x9F, 
	0x0A, 0xC5, 0x2E, 0x4F, 0xC8, 0x77, 0x88, 0x1B, 0x6E, 0x05, 
	0x03, 0x1D, 0x4C, 0xA9, 0xAB, 0x92, 0x0B, 0x1A, 0x62, 0xDC, 
	0x4E, 0x34, 0x66, 0x8D, 0xE0, 0x04, 0x74, 0x3F, 0x39, 0xD1, 
	0x3F, 0x89, 0x11, 0x98, 0x69, 0x02, 0x65, 0x53, 0x90, 0xFB, 
	0xEA, 0xA5, 0x4D, 0x7B, 0xA1, 0xE2, 0x80, 0xD5, 0xED, 0xA6, 
	0xDE, 0x4D, 0xC5, 0x94, 0x30, 0x24, 0xFB, 0x4E, 0x71, 0xE6, 
	0x0F, 0xE4, 0x4B, 0xA5, 0x1F, 0x1B, 0x6E, 0x56, 0x6D, 0x0A, 
	0x3D, 0x99, 0x4A, 0xAE, 0x14, 0x3E, 0x71, 0x84, 0xEE, 0xA9, 
	0x5B, 0xDE, 0x1C, 0x76, 0x87, 0x98, 0xC0, 0xED, 0x2D, 0x98, 
	0x62, 0x5F, 0x9A, 0x45, 0xD5, 0xA8, 0x69, 0x98, 0x9E, 0x25, 
	0x4A, 0x40, 0x2D, 0x53, 0xC1, 0x27, 0xA1, 0x93
    };
    static unsigned char dhg_1024[] = {
	0x02
    };
    DH *dh = DH_new();
    BIGNUM *dhp_bn, *dhg_bn;

    if (dh == NULL)
        return NULL;
    dhp_bn = BN_bin2bn(dhp_1024, sizeof (dhp_1024), NULL);
    dhg_bn = BN_bin2bn(dhg_1024, sizeof (dhg_1024), NULL);
    if (dhp_bn == NULL || dhg_bn == NULL
            || !DH_set0_pqg(dh, dhp_bn, NULL, dhg_bn)) {
        DH_free(dh);
        BN_free(dhp_bn);
        BN_free(dhg_bn);
        return NULL;
    }
    return dh;
}
