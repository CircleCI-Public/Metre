DH *get_dh1024()
{
    static unsigned char dhp_1024[] = {
	0x91, 0x9F, 0x58, 0x63, 0x9C, 0xEC, 0xD8, 0x0D, 0x00, 0x1B, 
	0x6B, 0xEA, 0x75, 0x8A, 0xDA, 0x78, 0x92, 0x3C, 0xCC, 0x03, 
	0x07, 0x8A, 0xD3, 0x45, 0xC5, 0x33, 0x55, 0xA4, 0x34, 0xE8, 
	0x82, 0x78, 0xEA, 0x33, 0x08, 0x8F, 0xE3, 0x09, 0xA2, 0x41, 
	0x1B, 0x72, 0xB2, 0x7B, 0xA9, 0xCB, 0x70, 0x1D, 0xE5, 0x29, 
	0x81, 0xFD, 0x5B, 0x4C, 0xC2, 0x33, 0xF3, 0x71, 0x81, 0x8C, 
	0xDD, 0x77, 0x76, 0xD7, 0xB0, 0x9F, 0x3E, 0x7A, 0xF4, 0xAE, 
	0xAE, 0xF2, 0xC5, 0xAD, 0x60, 0x99, 0x11, 0xF5, 0x76, 0x31, 
	0x99, 0x32, 0x0B, 0xE2, 0x36, 0x98, 0xFA, 0x6D, 0x72, 0xA1, 
	0x0A, 0x49, 0x88, 0x83, 0x0E, 0xEB, 0xDB, 0x47, 0xC8, 0x13, 
	0x94, 0xA5, 0x36, 0x76, 0x54, 0xD3, 0x4A, 0xCA, 0x04, 0x16, 
	0xC9, 0xFA, 0x1F, 0x95, 0x94, 0x8D, 0xFB, 0xE6, 0xAE, 0xEB, 
	0x66, 0xE9, 0x13, 0xF3, 0xD9, 0x26, 0x39, 0xC3
    };
    static unsigned char dhg_1024[] = {
	0x02
    };
    DH *dh = DH_new();
    BIGNUM *dhp_bn, *dhg_bn;

    if (dh == NULL)
        return NULL;
    dhp_bn = BN_bin2bn(dhp_1024, sizeof (dhp_1024), NULL);
    dhg_bn = BN_bin2bn(dhg_1024, sizeof (dhg_1024), NULL);
    if (dhp_bn == NULL || dhg_bn == NULL
            || !DH_set0_pqg(dh, dhp_bn, NULL, dhg_bn)) {
        DH_free(dh);
        BN_free(dhp_bn);
        BN_free(dhg_bn);
        return NULL;
    }
    return dh;
}
