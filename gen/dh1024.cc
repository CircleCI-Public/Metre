DH *get_dh1024()
{
    static unsigned char dhp_1024[] = {
	0xE7, 0xF0, 0x90, 0x1B, 0x26, 0x2F, 0x99, 0xC7, 0x28, 0xF2, 
	0x6F, 0xDC, 0xD4, 0xB6, 0x5C, 0x1F, 0xCE, 0xF8, 0x08, 0x49, 
	0xD1, 0xAB, 0xDA, 0x1A, 0xC9, 0x0D, 0x5B, 0x67, 0xFF, 0xD9, 
	0xF6, 0xD2, 0xC6, 0x85, 0x00, 0x9C, 0xF3, 0x49, 0x1B, 0x18, 
	0x7A, 0xC6, 0x66, 0x58, 0x84, 0xF0, 0xA0, 0xFB, 0xC8, 0x21, 
	0x60, 0x56, 0x54, 0x1D, 0x4F, 0x52, 0x87, 0xB0, 0xA9, 0xA3, 
	0x16, 0xF6, 0x97, 0x50, 0x85, 0xA9, 0x1A, 0xE9, 0x67, 0x3E, 
	0x02, 0xBB, 0xB3, 0xB9, 0xDF, 0x3E, 0xF3, 0xD7, 0x3D, 0x80, 
	0xD9, 0x4B, 0xA1, 0x1A, 0x94, 0xCC, 0x76, 0x7D, 0x4E, 0xDE, 
	0xA5, 0x66, 0x05, 0x19, 0x98, 0x31, 0x45, 0x70, 0x3E, 0x49, 
	0x1F, 0x4F, 0x0D, 0xA0, 0x32, 0x52, 0x71, 0x38, 0x4D, 0x1C, 
	0x50, 0xFE, 0x9C, 0xC6, 0xB9, 0xCA, 0x86, 0xEA, 0xBB, 0x76, 
	0xFD, 0xE2, 0xC6, 0x1A, 0x43, 0x29, 0xD9, 0xD3
    };
    static unsigned char dhg_1024[] = {
	0x02
    };
    DH *dh = DH_new();
    BIGNUM *dhp_bn, *dhg_bn;

    if (dh == NULL)
        return NULL;
    dhp_bn = BN_bin2bn(dhp_1024, sizeof (dhp_1024), NULL);
    dhg_bn = BN_bin2bn(dhg_1024, sizeof (dhg_1024), NULL);
    if (dhp_bn == NULL || dhg_bn == NULL
            || !DH_set0_pqg(dh, dhp_bn, NULL, dhg_bn)) {
        DH_free(dh);
        BN_free(dhp_bn);
        BN_free(dhg_bn);
        return NULL;
    }
    return dh;
}
